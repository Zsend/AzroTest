const galleryGrid = document.getElementById('galleryGrid');
const filterButtons = document.querySelectorAll('.filter-btn');
const lightbox = document.getElementById('lightbox');
const lightboxImg = document.getElementById('lightboxImg');
const lightboxCaption = document.getElementById('lightboxCaption');
const closeBtn = document.querySelector('.lightbox-close');
const prevBtn = document.querySelector('.lightbox-prev');
const nextBtn = document.querySelector('.lightbox-next');
const navToggle = document.querySelector('.nav-toggle');
const nav = document.querySelector('.nav');
let activePhotos = [];
let activeIndex = 0;

function visiblePhotos(filter) {
  if (filter === 'all') return photos;
  return photos.filter(photo => photo.tags.includes(filter));
}

function renderGallery(filter = 'featured') {
  activePhotos = visiblePhotos(filter);
  galleryGrid.innerHTML = '';
  if (!activePhotos.length) {
    galleryGrid.innerHTML = '<p class="muted">No photos in this category yet.</p>';
    return;
  }
  activePhotos.forEach((photo, index) => {
    const item = document.createElement('button');
    item.className = `gallery-item ${photo.shape || 'normal'}`;
    item.type = 'button';
    item.setAttribute('aria-label', `Open photo: ${photo.caption}`);
    item.innerHTML = `
      <img loading="lazy" src="${photo.thumb_path}" alt="${photo.caption}">
      <span>${photo.caption}</span>
    `;
    item.addEventListener('click', () => openLightbox(index));
    galleryGrid.appendChild(item);
  });
}

function openLightbox(index) {
  activeIndex = index;
  const photo = activePhotos[activeIndex];
  lightboxImg.src = photo.photo_path;
  lightboxImg.alt = photo.caption;
  lightboxCaption.textContent = `${photo.caption} (${activeIndex + 1} of ${activePhotos.length})`;
  lightbox.classList.add('open');
  lightbox.setAttribute('aria-hidden', 'false');
  document.body.style.overflow = 'hidden';
}

function closeLightbox() {
  lightbox.classList.remove('open');
  lightbox.setAttribute('aria-hidden', 'true');
  document.body.style.overflow = '';
  lightboxImg.src = '';
}

function stepLightbox(direction) {
  if (!activePhotos.length) return;
  activeIndex = (activeIndex + direction + activePhotos.length) % activePhotos.length;
  openLightbox(activeIndex);
}

filterButtons.forEach(button => {
  button.addEventListener('click', () => {
    filterButtons.forEach(btn => btn.classList.remove('active'));
    button.classList.add('active');
    renderGallery(button.dataset.filter);
  });
});
closeBtn.addEventListener('click', closeLightbox);
prevBtn.addEventListener('click', () => stepLightbox(-1));
nextBtn.addEventListener('click', () => stepLightbox(1));
lightbox.addEventListener('click', event => { if (event.target === lightbox) closeLightbox(); });
document.addEventListener('keydown', event => {
  if (!lightbox.classList.contains('open')) return;
  if (event.key === 'Escape') closeLightbox();
  if (event.key === 'ArrowLeft') stepLightbox(-1);
  if (event.key === 'ArrowRight') stepLightbox(1);
});
navToggle.addEventListener('click', () => {
  const expanded = navToggle.getAttribute('aria-expanded') === 'true';
  navToggle.setAttribute('aria-expanded', String(!expanded));
  nav.classList.toggle('open');
});
nav.querySelectorAll('a').forEach(link => {
  link.addEventListener('click', () => {
    nav.classList.remove('open');
    navToggle.setAttribute('aria-expanded', 'false');
  });
});
renderGallery('featured');
