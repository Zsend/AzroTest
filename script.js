const applicationOutput = document.getElementById('applicationOutput');
const formStatus = document.getElementById('formStatus');
const copyAppBtn = document.getElementById('copyAppBtn');

function buildApplicationMessage() {
  if (!applicationForm) return '';
  const data = new FormData(applicationForm);
  const lines = [
    'Doodles in Utah puppy list application',
    'Generated from DoodlesInUtah.com',
    '',
  ];
  for (const [key, value] of data.entries()) {
    if (key === 'Acknowledgment') continue;
    const clean = String(value || '').trim();
    if (clean) lines.push(`${key}: ${clean}`);
  }
  lines.push('', 'Acknowledgment: I understand this is a planned litter and deposits are not open yet.');
  return lines.join('\n');
}

async function copyText(text) {
  if (!text) return false;
  try {
    await navigator.clipboard.writeText(text);
    return true;
  } catch (err) {
    if (applicationOutput) {
      applicationOutput.hidden = false;
      applicationOutput.focus();
      applicationOutput.select();
    }
    return false;
  }
}

if (applicationForm) {
  applicationForm.addEventListener('submit', async (event) => {
    event.preventDefault();
    if (!applicationForm.reportValidity()) return;
    const message = buildApplicationMessage();
    if (applicationOutput) {
      applicationOutput.value = message;
      applicationOutput.hidden = false;
    }
    const copied = await copyText(message);
    const subject = encodeURIComponent('Puppy list application - Doodles in Utah');
    const body = encodeURIComponent(message);
    const mailto = `mailto:${CONTACT_EMAIL}?subject=${subject}&body=${body}`;
    if (formStatus) {
      formStatus.textContent = copied
        ? 'Application copied. An email draft should open next; you can also paste it into Instagram DM.'
        : 'Application generated below. Copy it and send by email or Instagram DM.';
    }
    window.location.href = mailto;
  });
}

if (copyAppBtn) {
  copyAppBtn.addEventListener('click', async () => {
    const message = buildApplicationMessage();
    if (applicationOutput) {
      applicationOutput.value = message;
      applicationOutput.hidden = false;
    }
    const copied = await copyText(message);
    if (formStatus) formStatus.textContent = copied ? 'Application copied. Paste it into email, text, or Instagram DM.' : 'Application generated below. Select and copy it manually.';
  });
}
