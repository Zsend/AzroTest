let currentFilter = 'all';
let visibleCount = 18;
let currentGallery = [];
let currentLightboxIndex = 0;
const galleryGrid = document.getElementById('galleryGrid');
const showMoreBtn = document.getElementById('showMoreBtn');
const filterButtons = document.querySelectorAll('.filter-btn');
const lightbox = document.getElementById('lightbox');
const lightboxImg = document.getElementById('lightboxImg');
const lightboxCaption = document.getElementById('lightboxCaption');

function getFilteredPhotos() {
  if (currentFilter === 'all') return PHOTOS;
  return PHOTOS.filter((photo) => (photo.tags || []).includes(currentFilter));
}

function renderGallery() {
  if (!galleryGrid) return;
  currentGallery = getFilteredPhotos();
  const visible = currentGallery.slice(0, visibleCount);
  galleryGrid.innerHTML = visible.map((photo, index) => `
    <button class="gallery-item ${photo.shape || ''}" type="button" data-index="${index}" data-caption="${escapeHTML(photo.caption || 'Doodles in Utah photo')}">
      <img src="${photo.thumb_path || photo.photo_path}" alt="${escapeHTML(photo.caption || 'Doodles in Utah photo')}" loading="lazy" />
    </button>
  `).join('');
  galleryGrid.querySelectorAll('.gallery-item').forEach((btn) => {
    btn.addEventListener('click', () => openLightbox(Number(btn.dataset.index)));
  });
  if (showMoreBtn) {
    showMoreBtn.style.display = visibleCount >= currentGallery.length ? 'none' : 'inline-flex';
  }
}

function escapeHTML(value) {
  return String(value).replace(/[&<>'"]/g, (char) => ({'&':'&amp;','<':'&lt;','>':'&gt;',"'":'&#39;','"':'&quot;'}[char]));
}

filterButtons.forEach((button) => {
  button.addEventListener('click', () => {
    filterButtons.forEach((btn) => btn.classList.remove('active'));
    button.classList.add('active');
    currentFilter = button.dataset.filter || 'all';
    visibleCount = currentFilter === 'all' ? 18 : 30;
    renderGallery();
  });
});
if (showMoreBtn) {
  showMoreBtn.addEventListener('click', () => {
    visibleCount += 16;
    renderGallery();
  });
}

function openLightbox(index) {
  if (!lightbox || !currentGallery[index]) return;
  currentLightboxIndex = index;
  const photo = currentGallery[index];
  lightboxImg.src = photo.photo_path;
  lightboxImg.alt = photo.caption || 'Doodles in Utah photo';
  lightboxCaption.textContent = photo.caption || '';
  lightbox.hidden = false;
  document.body.style.overflow = 'hidden';
}
function closeLightbox() {
  if (!lightbox) return;
  lightbox.hidden = true;
  lightboxImg.src = '';
  document.body.style.overflow = '';
}
function moveLightbox(step) {
  if (!currentGallery.length) return;
  currentLightboxIndex = (currentLightboxIndex + step + currentGallery.length) % currentGallery.length;
  openLightbox(currentLightboxIndex);
}
if (lightbox) {
  document.querySelector('.lightbox-close')?.addEventListener('click', closeLightbox);
  document.querySelector('.lightbox-arrow.prev')?.addEventListener('click', () => moveLightbox(-1));
  document.querySelector('.lightbox-arrow.next')?.addEventListener('click', () => moveLightbox(1));
  lightbox.addEventListener('click', (event) => { if (event.target === lightbox) closeLightbox(); });
  document.addEventListener('keydown', (event) => {
    if (lightbox.hidden) return;
    if (event.key === 'Escape') closeLightbox();
    if (event.key === 'ArrowLeft') moveLightbox(-1);
    if (event.key === 'ArrowRight') moveLightbox(1);
  });
}
